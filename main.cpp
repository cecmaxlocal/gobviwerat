// Here is a simple example of a `main.cpp` file 
// that serves as the entry point 
// for a C++ program:

// ```cpp
// main.cpp
#include <iostream>

using namespace std;

// Main function: program entry point
int main() {
    std::cout << "Hello, world!" << std::endl;
    return 0;
}
// ```

// ### Explanation:
// - The `main` function is the starting point of every C++ program.
// - `#include <iostream>` includes the standard input-output stream library.
// - `std::cout` prints output to the console.
// - `return 0;` indicates normal program termination.

